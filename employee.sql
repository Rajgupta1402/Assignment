CREATE DATABASE COMPANY;
USE COMPANY;
CREATE TABLE EMPLOYEE
(
EMP_ID INT PRIMARY KEY,
FIRST_NAME VARCHAR (35),
LAST_NAME VARCHAR(35),
D_O_B DATE,
GENDER ENUM("M","F"),
SALARY INT,
BRANCH_ID INT 
);

SELECT * FROM EMPLOYEE;

CREATE TABLE BRANCH
(
BRANCH_ID INT,
BRANCH_NAME VARCHAR (35),
